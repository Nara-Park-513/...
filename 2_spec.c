#include <stdio.h> //입출력 라이브러리

int main(){
int myNum = 15;
float myfloatNum = 5.99;
char myLetter = 'D';//단일문자

printf("%d\n", myNum);
printf("%f\n", myfloatNum);
printf("%c\n", myLetter);
return 0;//반납
}
/*
Format Specifiers 형식 지정자
%d = 숫자
%f = 소수점
%c = 단일문자
옛날에는 줄바꿈(개행문자)
\n
*/